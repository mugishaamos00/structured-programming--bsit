length= float(input("enter the length of the rectangle (in meters):"))
width= float(input("enter the width of the rectangle (in meters):"))


area = length * width
perimeter = 2 * (length + width)


print("\n===== Rectangle Measurements =====")
print(f"Length: {length} m")
print(f"Width: {width} m")
print(f"Area: {area} square meters")
print(f"Perimeter: {perimeter} meters")
